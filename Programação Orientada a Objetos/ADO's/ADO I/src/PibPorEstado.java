public class PibPorEstado {
    private String estado;
    private double pibPorEstado = 0.0;

    public PibPorEstado() {
    }

    public String getEstado() {
        return estado;
    }

    public void setEstado(String estado) {
        this.estado = estado;
    }

    public double getPibPorEstado() {
        return pibPorEstado;
    }

    public void setPibPorEstado(double pibPorEstado) {
        this.pibPorEstado = pibPorEstado;
    }
}
